#ifndef LSTD_TYPES_H
#define LSTD_TYPES_H
typedef unsigned char 		u8;
typedef unsigned short 		u16;
typedef unsigned int 		u32;
typedef unsigned long int 	u64;

typedef char 				s8;
typedef short 				s16;
typedef int 				s32;
typedef long int 			s64;

typedef unsigned int		uint;
typedef int 				sint;
#endif