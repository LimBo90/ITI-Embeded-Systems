#ifndef HLCD_PRIVATE_H
#define HLCD_PRIVATE_H

//Function Set
#define FNSET_DATA_LENGTH_8_BIT 0b00110000
#define FNSET_DATA_LENGTH_4_BIT 0b00100000
#define FNSET_TWO_LINE_DISPLAY	0b00101000
#define FNSET_ONE_LINE_DISPLAY	0b00100000
#define FNSET_FONT_5x7	0b00100100
#define FNSET_FONT_5x10	0b00100000

//Display ON/OFF Control
#define DISPLAYCONTROL_DISPLAY_ON 0b00001100
#define DISPLAYCONTROL_DISPLAY_OFF 0b00001000
#define DISPLAYCONTROL_CURSOR_ON 0b00001010
#define DISPLAYCONTROL_CURSOR_OFF 0b00001000
#define DISPLAYCONTROL_CURSOR_BLINK_ON 0b00001001
#define DISPLAYCONTROL_CURSOR_BLINK_OFF 0b00001001

//clear display
#define CLEAR_DISPLAY 1

//return home
#define RETURN_HOME 0b00000010

//entry mode
#define ENTRYMODE_LEFT_TO_RIGHT 0b00000110
#define ENTRYMODE_RIGHT_TO_LEFT 0b00000100
#define ENTRYMODE_DISPLAY_SHIFT_ON 0b00000101
#define ENTRYMODE_DISPLAY_SHIFT_OFF 0b00000100

//set DDRAM address
#define DDRAM_FIRST_LINE_START_ADDRESS 0x80
#define DDRAM_SECOND_LINE_START_ADDRESS 0xC0

//set CGRAM address
#define SET_CGRAM_ADDRESS_CMD 0x40

//cursor or display shift
#define SHIFT_CURSOR_LEFT	0x10
#define SHIFT_CURSOR_RIGHT	0x14
#define SHIFT_DISPLAY_LEFT	0x18
#define SHIFT_DISPLAY_RIGHT	0x1C


#endif
